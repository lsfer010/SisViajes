    private void agregar() {
        boolean ap_Paterno_Existe = this.ComprobarApPaterno();
        boolean nombre_Existe = this.ComprobarNombre();
        boolean Digitos_Fecha_Bien = true;
        boolean Fecha_Bien = true;
        
        if (!(tDia.getText().trim().equals("") && tMes.getText().trim().equals("") && tAnio.getText().trim().equals(""))) {
           Digitos_Fecha_Bien = this.RevisarDigitosFecha();
           Fecha_Bien = this.RevisarFecha(); 
        }
        
        if(ap_Paterno_Existe && nombre_Existe && Digitos_Fecha_Bien && Fecha_Bien){
            if((tDia.getText().trim().equals("") && tMes.getText().trim().equals("") && tAnio.getText().trim().equals(""))
                || (tDia.getText().trim().length()>0 && tMes.getText().trim().length()>0 && tAnio.getText().trim().length()>0)) {
               
               String compañia = tCompania.getText(), 
               apellido_Paterno = tApPaterno.getText(), 
               apellido_Materno = tApMaterno.getText(), 
               nombre = tNombre.getText(), 
               dia = tDia.getText(), 
               mes = tMes.getText(), 
               año = tAnio.getText(), 
               fecha_nacimiento = dia + "/" + mes + "/" + año, 
               sexo = tSexo.getSelectedItem().toString();
           
            int no_Club_Premier;
            if(!tNoClubPremier.getText().equals("")) {
                no_Club_Premier = Integer.parseInt(tNoClubPremier.getText());
            } else {
                no_Club_Premier = 0;
            }
            
            if(tDia.getText().trim().equals("") && tMes.getText().trim().equals("") && tAnio.getText().trim().equals("")) {
               fecha_nacimiento = "";     
            }
            
            Altas operacion = new Altas();
            operacion.alta(compañia, apellido_Paterno, apellido_Materno, nombre, fecha_nacimiento, sexo, no_Club_Premier);
            borrarDatos();
            } else {
                JOptionPane.showMessageDialog(null, "Se detecto que en la fecha falta especificar un dato. Se debe especificar dia, mes y año en el siguiente formato: DD/MM/AAAA.","Operación fallida", JOptionPane.WARNING_MESSAGE);
            }    
        } else {
            
            if(!ap_Paterno_Existe) {
               JOptionPane.showMessageDialog(null, "El campo <Apellido Paterno> es obligatorio","Operación fallida", JOptionPane.WARNING_MESSAGE);
            }
            
            if(!nombre_Existe) {
               JOptionPane.showMessageDialog(null, "El campo <Nombre> es obligatorio","Operación fallida", JOptionPane.WARNING_MESSAGE); 
            }
            
            if(!Digitos_Fecha_Bien){
               JOptionPane.showMessageDialog(null, "El formato de la fecha no es correcto. El formato debe ser como sigue: DD/MM/AAAA (dos digitos para el dia, dos digitos para el mes, cuatro digitos para el año)","Operación fallida", JOptionPane.WARNING_MESSAGE);
            }
            
            if(!Fecha_Bien){
               JOptionPane.showMessageDialog(null, "La fecha no es correcta. Revise si: el dia no supere 30 o 31 dias dependiendo el mes, o bien 28 si es febrero y no es año bisiesto, o 29 si es año bisiesto; el mes debe estar entre 01 y 12; el año debe ser mayor a 0","Operación fallida", JOptionPane.WARNING_MESSAGE);
            }
        }   
    }
    
