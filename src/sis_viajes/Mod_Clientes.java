    private void actualizarTabla() {
        try {
            cantidad_id = 0;
            
            DefaultTableModel modelo = new CustomTableModel();
            jTClientes.setModel(modelo);
            
            
            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion connection = new Conexion();
            Connection con = connection.getConexion();
            
            String sql = "SELECT Apellido_Paterno, Apellido_Materno, Nombre, Compañia, Fecha_Nacimiento, Sexo, No_Club_Premier FROM clientes";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            modelo.addColumn("Apellido Paterno");
            modelo.addColumn("Apellido Materno");
            modelo.addColumn("Nombre");
            modelo.addColumn("Compañia");
            modelo.addColumn("Fecha Nacimiento");
            modelo.addColumn("Sexo");
            modelo.addColumn("No. Club Premier");
            
            for (int i=0; i <= 6; i++) {
              jTClientes.getColumnModel().getColumn(i).setPreferredWidth(tamaño_Columnas);  
            }
             
            while(rs.next()) {
                Object[] filas = new Object[cantidadColumnas]; //ya que la tabla trabaja con objetos, metemos todo en objetos
                
                for (int i=0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                
                cantidad_id++;
                modelo.addRow(filas);
            }
            
            con.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error:" + e);
        }
        
        id_clientes = new int[cantidad_id];
        
        this.listarID();
        
    }
    

