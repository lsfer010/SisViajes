    private void jTClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTClientesMouseClicked
        PreparedStatement ps = null;
        ResultSet rs = null;
        try {
            Conexion connection = new Conexion();
            Connection con = connection.getConexion();

            int Fila = jTClientes.getSelectedRow();
            id_cliente_actual = id_clientes[Fila]+"";

            ps = con.prepareStatement("SELECT Apellido_Paterno, Apellido_Materno, Nombre, Compañia, Fecha_Nacimiento, Sexo, No_Club_Premier FROM clientes WHERE ID_Cliente = ?");
            ps.setInt(1, Integer.parseInt(id_clientes[Fila]+""));
            rs = ps.executeQuery();

            String[] fecha_parte;
            while (rs.next()) {
                tApPaternoMod.setText(rs.getString("Apellido_Paterno"));
                tApMaternoMod.setText(rs.getString("Apellido_Materno"));
                tNombreMod.setText(rs.getString("Nombre"));
                tCompañiaMod.setText(rs.getString("Compañia"));
                
                if(Integer.parseInt(rs.getString("No_Club_Premier")) != 0) {
                    tNoClubPremierMod.setText(rs.getString("No_Club_Premier"));
                } else {
                    tNoClubPremierMod.setText(""); 
                }
                
                if(!rs.getString("Fecha_Nacimiento").trim().equals("")) {
                   fecha_parte = rs.getString("Fecha_Nacimiento").split("/");
                    tDiaMod.setText(fecha_parte[0]);
                    tMesMod.setText(fecha_parte[1]);
                    tAnioMod.setText(fecha_parte[2]); 
                } else {
                    tDiaMod.setText("");
                    tMesMod.setText("");
                    tAnioMod.setText(""); 
                }
                tSexoMod.setSelectedItem(rs.getString("Sexo"));

            }
            con.close();
        } catch (SQLException ex) {
            JOptionPane.showInputDialog("Ocurrio un error: " + ex.toString());
        }
    }//GEN-LAST:event_jTClientesMouseClicked

